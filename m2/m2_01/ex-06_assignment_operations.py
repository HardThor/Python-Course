"""Операції з присвоєнням.
Коли ти бачиш +=, -=, *=, /=, то це скорочений запис операції з присвоєнням.
Суть:
Виконується дія (додавання, віднімання тощо).
Одразу оновлюється значення змінної.
Наприклад:
x = 5
x += 3  # Це те саме, що: x = x + 3
print(x)

Аналогічно для інших операторів:

+= → додавання і присвоєння
-= → віднімання і присвоєння
*= → множення і присвоєння
/= → ділення і присвоєння
**= → піднесення до степеня і присвоєння
%= → остача від ділення і присвоєння

Це працює і з рядками:

text = "Hello"
text += " World!"  # text = text + " World!"
print(text)  # Виведе "Hello World!"
"""
sum = 0
a = 1 
while a <= 5:
    sum += a
    a = a + 1
print(sum) # 15

"""Чому не просто sum + a? — це нічого не змінить у змінній sum.
Python просто порахує результат і викине його, бо ти його нікуди не зберігаєш. 
Оператор += дозволяє одночасно додати значення і оновити змінну. Це робить код коротшим і зрозумілішим!"""