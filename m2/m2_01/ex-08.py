"""Парність і непарність
Парне число — це число, яке ділиться на 2 без залишку.
Непарне число — це число, яке не ділиться на 2 без залишку.

Перевірка парності:
if number % 2 == 0:
    print("Число парне")

Перевірка непарності:
if number % 2 != 0:
    print("Число непарне")

number % 3 == 0 НЕ перевіряє непарність!
Кратність числа
Кратність — це коли число ділиться без залишку на інше число.
if number % 3 == 0: перевіряє, чи число кратне 3.
if number % 5 == 0: перевіряє, чи число кратне 5.
if number % 10 == 0: перевіряє, чи число кратне 10.

Приклад:
6 кратне 3, бо 6 % 3 == 0.
9 кратне 3, бо 9 % 3 == 0.
10 не кратне 3, бо 10 % 3 != 0.

Чому number % 3 == 0 не означає "непарність"?
Бо є непарні числа, які не кратні 3:
1, 5, 7, 11, 13... — це непарні числа, але вони не діляться на 3 без залишку.
Наприклад, 5 % 3 != 0, а отже, 5 не ділиться на 3 без залишку, хоча воно непарне.
І є парні числа, які кратні 3:

6, 12, 18... — це парні числа, але вони діляться на 3 без залишку.
Тому "непарність" і "кратність 3" — це різні речі!"""


first = int(input("Enter the first number: "))
second = int(input("Enter the second number: "))

if first > second:
    print("The first number is greater than the second number.") # Перше число більше другого.
elif first == second:
    print("The first number is equal to the second number.") # Перше число дорівнює другому числу.
else:
    print("The second number is greater than the first number.") # Друге число більше першого числа.


if first % 2 == 0:
    print("The first number is odd.") # Перше число непарне.
else:
    print("The first number is even.") # Перше число парне.


if second % 3 == 0: # ==0 - це явна перевірка на рівність нулю
    print("Число не кратно 3.")
else:
    print("Число кратно 3.")


